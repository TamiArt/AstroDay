// Вспомогательная функция для получения полной интерпретации
export function getPlanetInterpretation(
  planetName: string,
  signName: string,
  houseNumber: number
): { sign: PlanetInSignInterpretation | null; house: PlanetInHouseInterpretation | null } {
  let signInterp: PlanetInSignInterpretation | null = null;
  let houseInterp: PlanetInHouseInterpretation | null = HOUSES_MEANING[houseNumber] || null;

  // Выбираем правильный словарь интерпретаций
  if (planetName === 'Sun') {
    signInterp = SUN_IN_SIGNS[signName] || null;
  } else if (planetName === 'Moon') {
    signInterp = MOON_IN_SIGNS[signName] || null;
  } else if (planetName === 'Mercury') {
    signInterp = getMercuryInSign(signName);
  } else if (planetName === 'Venus') {
    signInterp = getVenusInSign(signName);
  } else if (planetName === 'Mars') {
    signInterp = getMarsInSign(signName);
  } else if (planetName === 'Jupiter') {
    signInterp = getJupiterInSign(signName);
  } else if (planetName === 'Saturn') {
    signInterp = getSaturnInSign(signName);
  } else if (planetName === 'Rahu') {
    signInterp = getRahuInSign(signName);
  } else if (planetName === 'Ketu') {
    signInterp = getKetuInSign(signName);
  } else if (planetName === 'Asc') {
    signInterp = getAscendantInSign(signName);
  }

  signInterp = buildFormulaInterpretation(
    planetName,
    signName,
    signInterp || buildPlanetInSignInterpretation(planetName, signName)
  );

  return { sign: signInterp, house: houseInterp };
}

