// Получить интерпретацию аспекта
export function getDetailedAspectInterpretation(
  planet1: string,
  planet2: string,
  aspectType: string
): AspectInterpretation {
  const key1 = `${planet1}-${planet2}`;
  const key2 = `${planet2}-${planet1}`;

  return ASPECT_INTERPRETATIONS[key1]?.[aspectType] ||
         ASPECT_INTERPRETATIONS[key2]?.[aspectType] ||
         createFallbackAspectInterpretation(planet1, planet2, aspectType);
}

function createFallbackAspectInterpretation(
  planet1: string,
  planet2: string,
  aspectType: string
): AspectInterpretation {
  const aspectTone: Record<string, { meaning: string; advice: string; challenge?: string }> = {
    conjunction: {
      meaning: 'две планеты работают в одной точке карты, усиливая друг друга и требуя осознанного фокуса',
      advice: 'Соберите энергию в один конкретный план вместо распыления.',
    },
    sextile: {
      meaning: 'между планетами есть мягкая возможность для сотрудничества',
      advice: 'Используйте этот аспект через разговор, обучение или небольшой практический шаг.',
    },
    trine: {
      meaning: 'планеты поддерживают друг друга естественно и раскрывают врождённый ресурс',
      advice: 'Опирайтесь на этот талант, но не оставляйте его без практики.',
    },
    square: {
      meaning: 'между планетами есть напряжение, которое подталкивает к росту через действие',
      advice: 'Сначала снижайте импульс, затем переводите напряжение в конструктивную задачу.',
      challenge: 'Поспешность, внутренний конфликт, давление на себя или других.',
    },
    opposition: {
      meaning: 'планеты стоят напротив друг друга и требуют баланса между двумя полюсами',
      advice: 'Ищите не победителя, а честное равновесие между обеими темами.',
      challenge: 'Качели, проекции, крайности в решениях.',
    },
  };
  const tone = aspectTone[aspectType] || aspectTone.conjunction;

  return {
    meaning: `${planet1} и ${planet2}: ${tone.meaning}. Это персональная точка карты, где важно понять, как две функции психики взаимодействуют именно у вас.`,
    spheres: ['Личный выбор', 'Поведение', 'Отношения с темой планет'],
    recommendations: [
      tone.advice,
      `Наблюдайте, в каких ситуациях одновременно включаются ${planet1} и ${planet2}.`,
      'Записывайте повторяющиеся сценарии: так аспект становится понятным и управляемым.',
    ],
    benefits: ['Осознанность', 'Рост через практику', 'Лучшее понимание собственных реакций'],
    challenges: tone.challenge ? [tone.challenge] : undefined,
  };
}
